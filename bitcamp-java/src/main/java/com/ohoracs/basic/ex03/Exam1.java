    //정수 리터럴
    System.out.println(78); //10진수 표기법
    //78이 정수 리터럴임
    System.out.println(+78); //양수 리터럴
    System.out.println(-78); //음수 리터럴

    System.out.println(0116); //8진수 표기법
    System.out.println(-0116); //8진수 표기법

    System.out.println(0x4e); //16진수 표기법
    System.out.println(-0x4e); //16진수 표기법

    System.out.println(0b01001110); //2진수 표기법

    
